const num1 = parseInt(prompt('Enter the first number '));
const num2 = parseInt(prompt('Enter the second number '));
const sum = num1 + num2;
const sub = num1 - num2;
const mul = num1 * num2;
const div = num1 / num2;
const mod = num1 % num2;


console.log(`Addition ${num1} + ${num2} = ${sum}`);
console.log(`Subtraction ${num1} - ${num2} = ${sub}`);
console.log(`Multiplication ${num1} * ${num2} = ${mul}`);
console.log(`Division ${num1} / ${num2} = ${div}`);
console.log(`Modulo ${num1} % ${num2} = ${mod}`);

